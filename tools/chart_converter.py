from dataclasses import dataclass, field

# ── Constants mirroring the C++ engine ────────────────────────────────────────
TIMELINE_MAX     = 0xFFFF
MAX_NOTES        = 512  # MAX_NOTES_PER_CHART from NoteChart.h
LANE_MAP: dict[str, int] = {'L': 0, 'R': 1, 'U': 2, 'D': 3}
FLAG_MAP: dict[str, int] = {
    'mine':   0x01,
    'fake':   0x02,
    'accent': 0x04,
}
DEFAULT_METER_NUMERATOR = 4  # 4/4 time

@dataclass(order=True)
class NoteRow:
    """One emitted C++ Note struct, fully resolved to integer fields."""
    sort_index: tuple = field(init=False, repr=False)
    timeline:    int  = 0       # uint16_t Q8.8
    hold_length: int  = 0       # uint16_t Q8.8, 0 = tap
    lane:        int  = 0       # uint8_t  0–3
    flags:       int  = 0       # uint8_t  bitmask
    source_time: str  = ""      # original time string, for error messages

    def __post_init__(self):
        self.sort_index = (self.timeline, self.lane)

class TickEncoder:
    def __init__(self, total_bars: int, meter_numerator: int = DEFAULT_METER_NUMERATOR):
        if 0x10000 % total_bars != 0:
            raise ValueError(
                f"total_bars={total_bars} does not evenly divide 0x10000. "
                f"Chart bars must be a power-of-two for exact tick mapping."
            )
        self.ticks_per_bar  = 0x10000 // total_bars
        self.ticks_per_beat = self.ticks_per_bar // meter_numerator
        self.ticks_per_16th = self.ticks_per_beat // 4

    def encode(self, time_str: str) -> int:
        parts = time_str.strip().split(':')
        if len(parts) != 3:
            raise ValueError(f"Time string '{time_str}' must be 'bar:beat:sixteenth'")
        try:
            bar, beat, sixteenth = int(parts[0]), int(parts[1]), int(parts[2])
        except ValueError:
            raise ValueError(f"Time string '{time_str}' contains non-integer components.")

        tick = (bar * self.ticks_per_bar) + (beat * self.ticks_per_beat) + (sixteenth * self.ticks_per_16th)

        if not (0 <= tick <= TIMELINE_MAX):
            raise ValueError(f"Time string '{time_str}' resolves to tick 0x{tick:04X} (out of bounds).")
        return tick

    def encode_duration(self, duration_str: str) -> int:
        parts = duration_str.strip().split(':')
        if len(parts) != 3:
            raise ValueError(f"Hold duration '{duration_str}' must be 'bar:beat:sixteenth'.")
        try:
            bar, beat, sixteenth = int(parts[0]), int(parts[1]), int(parts[2])
        except ValueError:
            raise ValueError(f"Hold duration '{duration_str}' contains non-integer components.")
        return (bar * self.ticks_per_bar) + (beat * self.ticks_per_beat) + (sixteenth * self.ticks_per_16th)

def assemble_flags(flag_list: list[str]) -> int:
    result = 0
    for name in flag_list:
        if name not in FLAG_MAP:
            raise ValueError(f"Unknown flag '{name}'. Valid flags: {list(FLAG_MAP.keys())}")
        result |= FLAG_MAP[name]
    return result

def expand_note(entry: dict, encoder: TickEncoder) -> list[NoteRow]:
    time_str  = entry.get("time", "")
    lane_str  = entry.get("lane", "")
    hold_str  = entry.get("hold", "0:0:0")
    flag_list = entry.get("flags", [])

    if not time_str or not lane_str:
        raise ValueError(f"Note entry missing time or lane fields: {entry}")
    if len(set(lane_str)) != len(lane_str):
        raise ValueError(f"Lane string '{lane_str}' contains duplicate characters.")

    for ch in lane_str:
        if ch not in LANE_MAP:
            raise ValueError(f"Unknown lane character '{ch}' in '{lane_str}'.")

    timeline    = encoder.encode(time_str)
    hold_length = encoder.encode_duration(hold_str)
    flags       = assemble_flags(flag_list)

    if hold_length > 0 and (timeline + hold_length) > TIMELINE_MAX:
        print(f"  WARNING: Hold at '{time_str}' overflows 0xFFFF boundary. Clamping will apply.", file=sys.stderr)

    rows = []
    for ch in lane_str:
        rows.append(NoteRow(
            timeline    = timeline,
            hold_length = hold_length,
            lane        = LANE_MAP[ch],
            flags       = flags,
            source_time = time_str
        ))
    return rows

