LANE_NAMES = {0: 'L', 1: 'R', 2: 'U', 3: 'D'}

def emit_cpp(rows: list[NoteRow], song_id: int, bpm: int, bars: int, encoder: TickEncoder, source_json: str, output_path: str) -> None:
    song_id_padded = f"{song_id:02d}"
    filename       = os.path.basename(output_path)
    note_count     = len(rows)

    with open(output_path, 'w', encoding='utf-8') as f:
        f.write(CPP_HEADER_TEMPLATE.format(
            filename       = filename,
            source_json    = os.path.basename(source_json),
            song_id        = song_id,
            song_id_padded = song_id_padded,
            bpm            = bpm,
            bars           = bars,
            note_count     = note_count,
            ticks_per_bar  = encoder.ticks_per_bar,
            ticks_per_beat = encoder.ticks_per_beat,
            ticks_per_16th = encoder.ticks_per_16th,
        ))

        for row in rows:
            lane_char = LANE_NAMES[row.lane]
            hold_type = f"hold {row.hold_length:#06x}" if row.hold_length else "tap"
            comment   = f"t={row.source_time} lane={lane_char} {hold_type}"

            f.write(CPP_NOTE_ROW.format(
                timeline = row.timeline,
                hold     = row.hold_length,
                lane     = row.lane,
                flags    = row.flags,
                comment  = comment,
            ))

        f.write(CPP_FOOTER_TEMPLATE.format(
            song_id_padded = song_id_padded,
            song_id        = song_id,
            bpm            = bpm,
            max_notes      = MAX_NOTES,
        ))

